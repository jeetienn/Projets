#include "minishell.h"

int	is_valid_identifier(const char *str)
{
	int	i;

	if (!str || !*str)
		return (0);
	if (!ft_isalpha(str[0]) && str[0] != '_')
		return (0);
	i = 1;
	while (str[i])
	{
		if (!ft_isalnum(str[i]) && str[i] != '_')
			return (0);
		i++;
	}
	return (1);
}

int	ft_isdigit_str(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}

int	check_echo_option(char *arg)
{
	int	j;

	j = 1;
	while (arg[j])
	{
		if (arg[j] != 'n')
			return (0);
		j++;
	}
	if (j > 1)
		return (1);
	return (0);
}

void	free_env_node(t_env *node)
{
	free(node->key);
	free(node->value);
	free(node);
}

char	*extract_key(char *arg, char *eq)
{
	if (eq)
		return (ft_strndup(arg, eq - arg));
	return (ft_strdup(arg));
}
